// value can't be changed
const hubby = "Buffy Khan";
console.log(hubby);

// elements of an array can be changed
const numbers = [12, 45];
numbers[1] = 88;
console.log(numbers);

//push can be used to add elements in an array
const numbers = [12, 45];
numbers[1] = 88;
numbers.push(12);
console.log(numbers);
